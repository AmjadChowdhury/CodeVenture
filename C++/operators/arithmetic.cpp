#include<iostream>
using namespace std;

int main()
{
    int x = 12;
    int y = 10;
    int z;

    z = x + y;
    cout<<"Addition -> "<<z<<endl;

    z = x - y;
    cout<<"Subtraction -> "<<z<<endl;

    z = x * y;
    cout<<"Multiplication -> "<<z<<endl;

    z = x / y;
    cout<<"division -> "<<z<<endl;

    z = x % y;
    cout<<"Modulus -> "<<z<<endl;

    return 0;
}